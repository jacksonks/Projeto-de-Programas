package uea.est.edu.br.Data;

public class Data {
    private int Dia;
    private int Mes;
    private int Ano;

    public int getDia() {
	return Dia;
    }

    public void setDia(int Dia) {
	this.Dia = Dia;
    }
    
    public int getMes() {
	return Mes;
    }

    public void setMes(int Mes) {
	this.Mes = Mes;
    }
    
    public int getAno() {
	return Ano;
    }

    public void setAno(int Ano) {
	this.Ano = Ano;
    }    
}
